--considerAscentDeeper has depths II as 6, maus as 7, ascent maus as 8, depths II as 9, etc.
--by default blue womb is 8.5 instead of 9, and sheol is 9 instead of 10, includeBlueWomb sets it back to normal
--by default alt floors (ie. glacier, downpour) have 1 added, dontAdjustAltFloors forces this not to happen
function mod:getFloorDepth(considerAscentDeeper, includeBlueWomb, dontAdjustAltFloors)
	local level = Game():GetLevel()
	local levelNum = level:GetStage()
	local stageType = level:GetStageType()
	if StageAPI and StageAPI.InNewStage() then
		local stage = StageAPI.GetCurrentStage()
		if dontAdjustAltFloors then
			levelNum = stage.StageHPNumber
		else
			levelNum = stage.StageNumber
		end
	elseif stageType == StageType.STAGETYPE_REPENTANCE or stageType == StageType.STAGETYPE_REPENTANCE_B then
		if not dontAdjustAltFloors then
			levelNum = levelNum + 1
		end
	elseif not includeBlueWomb then
		if levelNum == LevelStage.STAGE4_3 then
			levelNum = levelNum - 0.5
		elseif levelNum > LevelStage.STAGE4_3 and levelNum < LevelStage.STAGE8 then
			levelNum = levelNum - 1
		end
	end
	if considerAscentDeeper then
		if levelNum == LevelStage.STAGE8 then
			levelNum = 15
		elseif Game():GetStateFlag(GameStateFlag.STATE_BACKWARDS_PATH) then
			levelNum = 7 + (8 - levelNum)
		end
	elseif levelNum == LevelStage.STAGE8 then
		levelNum = 0
	end
	
	-- FiendFolio.OverriddenFloorDepth is for anyone
	-- who has a custom floor not properly handled here
	-- that wants to include their own compatibility
	return FiendFolio.OverriddenFloorDepth or levelNum
end

